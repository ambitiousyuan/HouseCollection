import com.hnshengen.housecollection.house.core.common.controller.JiziErrorView;
import com.hnshengen.housecollection.house.core.interceptor.AttributeSetInteceptor;
import com.hnshengen.housecollection.house.core.interceptor.RestApiInteceptor;
